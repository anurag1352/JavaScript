// && Logical operator

let marks = 85;

if (marks >= 33 && marks >= 80){
    console.log("Pass");
    console.log("Grade : A");
}

// || Logical operator

let mark = 35;

if (mark >= 33 || mark >= 80){
    console.log("Pass");
    console.log("Grade : A+");
}

// ! Logical operator

let mark2 = 35;

if (!(mark2 < 33)){
    console.log("Pass");
    console.log("Grade : A+");
}